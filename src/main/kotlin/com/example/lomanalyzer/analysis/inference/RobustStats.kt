/*
 * НАЗНАЧЕНИЕ
 * Базовый набор функций робастной (устойчивой к выбросам) статистики —
 * фундамент этапов 7-8 пайплайна (оценки и неопределённость). Используется
 * как z-нормализацией композитов, так и бутстрапом (квантильные интервалы).
 * Соответствует Приложению Е.1-Е.2 диплома (см. docs/formulas.md).
 *
 * ЧТО ВНУТРИ
 * Объект-синглтон RobustStats со статическими функциями:
 *   - median(values)        — медиана (Е.1);
 *   - iqr(values)           — межквартильный размах IQR, тип 7 (Е.1);
 *   - quantile(values, p)   — квантиль методом линейной интерполяции (тип 7);
 *   - mad(values)           — нормированное медианное абсолютное отклонение MAD (Е.2);
 *   - huberMEstimate(...)   — M-оценка положения Хьюбера через IRLS (Е.2).
 * И фиксированные параметры: MAD_CONSTANT=1.4826, HUBER_K=1.345, tol=1e-6.
 *
 * МЕТОД
 * Робастные оценки заменяют среднее/СКО на медиану/IQR/MAD, чтобы выбросы
 * (вирусные посты, боты) не искажали статистику. Квантили считаются по типу 7
 * (Hyndman-Fan) — поведение по умолчанию в R и большинстве пакетов. M-оценка
 * Хьюбера — компромисс между средним (эффективность при норме) и медианой
 * (устойчивость): даёт ~95% эффективности при нормальном распределении.
 *
 * БИБЛИОТЕКИ
 * Только stdlib Kotlin (kotlin.math.abs/sqrt) — внешних зависимостей нет.
 *
 * СВЯЗИ
 * Вызывается из OneLevelBootstrap/TwoLevelBootstrap (quantile для percentile-CI),
 * из RobustZScore (median/iqr) и CompositeScorer (median для порогов θ).
 */
package com.example.lomanalyzer.analysis.inference

import kotlin.math.abs
import kotlin.math.sqrt

/**
 * Функции робастной статистики по Приложению Е.1-Е.2 диплома.
 *
 * Все реализации следуют каноническим определениям; параметры (c, k, tol)
 * зафиксированы в дипломе и обоснованы ссылками [75, 76].
 */
object RobustStats {

    /**
     * Медиана (Приложение Е.1).
     * Для чётного n — среднее двух центральных значений.
     * @param values выборка; пустая выборка даёт 0.0.
     * @return медиана выборки.
     */
    fun median(values: List<Double>): Double {
        if (values.isEmpty()) return 0.0
        val sorted = values.sorted()            // медиана требует упорядоченной выборки
        val n = sorted.size
        return if (n % 2 == 1) sorted[n / 2]    // нечётное n: единственный центральный элемент
        else (sorted[n / 2 - 1] + sorted[n / 2]) / 2.0  // чётное n: среднее двух центральных
    }

    /**
     * Межквартильный размах (IQR), тип 7 Hyndman-Fan (Приложение Е.1).
     * IQR = Q75 - Q25; это значение по умолчанию в R и большинстве пакетов.
     * Служит робастной мерой разброса (аналог СКО) при z-нормализации.
     * @return ширина «среднего» 50% выборки; при n < 2 возвращает 0.0.
     */
    fun iqr(values: List<Double>): Double {
        if (values.size < 2) return 0.0
        // Разница 0.75- и 0.25-квантилей = диапазон, в который попадает центральная половина данных
        return quantile(values, 0.75) - quantile(values, 0.25)
    }

    /**
     * Квантиль уровня p методом линейной интерполяции тип 7 (Hyndman-Fan).
     * Позиция: h = (n-1)*p; значение интерполируется между соседними порядковыми
     * статистиками: x[floor(h)] + frac * (x[ceil(h)] - x[floor(h)]).
     * @param p уровень квантиля в [0..1] (например, 0.025 и 0.975 для 95% CI).
     * @return значение квантиля; пустая выборка даёт 0.0.
     */
    fun quantile(values: List<Double>, p: Double): Double {
        require(p in 0.0..1.0)                   // уровень квантиля обязан лежать в [0..1]
        if (values.isEmpty()) return 0.0
        val sorted = values.sorted()             // квантиль определяется по упорядоченной выборке
        val n = sorted.size
        if (n == 1) return sorted[0]             // единственное значение и есть любой квантиль

        val h = (n - 1) * p                      // дробная позиция квантиля в массиве (тип 7)
        val lo = h.toInt()                       // индекс нижней соседней порядковой статистики
        val hi = (lo + 1).coerceAtMost(n - 1)    // индекс верхней соседней (с защитой от выхода за край)
        val frac = h - lo                        // дробная часть позиции — вес линейной интерполяции
        // Линейная интерполяция между двумя соседними значениями выборки
        return sorted[lo] + frac * (sorted[hi] - sorted[lo])
    }

    /**
     * Нормированное медианное абсолютное отклонение MAD (Приложение Е.2).
     * MAD_n = c * med_i |x_i - med_j(x_j)|, где c = 1.4826 — константа
     * состоятельности, делающая MAD несмещённой оценкой СКО для нормального
     * распределения. Робастная (устойчивая к выбросам) мера масштаба.
     * @return оценка масштаба выборки; при n < 2 возвращает 0.0.
     */
    fun mad(values: List<Double>): Double {
        if (values.size < 2) return 0.0
        val med = median(values)                          // центр выборки — медиана
        val absDeviations = values.map { abs(it - med) }  // абсолютные отклонения от медианы
        // Медиана абсолютных отклонений, масштабированная константой c для согласия с СКО
        return MAD_CONSTANT * median(absDeviations)
    }

    /**
     * M-оценка положения Хьюбера (Приложение Е.2) через IRLS
     * (Iteratively Reweighted Least Squares — итеративно перевзвешиваемый МНК).
     *
     * Идея: на каждой итерации каждому наблюдению назначается вес w в зависимости
     * от его стандартизованного остатка r = (x-mu)/s. Наблюдения внутри «ядра»
     * (|r| <= k) получают вес 1, далёкие выбросы (|r| > k) — уменьшенный вес k/|r|,
     * что ограничивает их влияние. Затем mu пересчитывается как взвешенное среднее.
     *
     * @param k настроечная константа, по умолчанию 1.345 (~95% эффективность при норме).
     * @param tol относительный порог сходимости, 1e-6 по диплому.
     * @param maxIter максимум итераций (страховка от незавершения).
     * @return робастная оценка центра выборки.
     */
    fun huberMEstimate(
        values: List<Double>,
        k: Double = HUBER_K,
        tol: Double = HUBER_TOL,
        maxIter: Int = HUBER_MAX_ITER,
    ): Double {
        if (values.isEmpty()) return 0.0
        if (values.size == 1) return values[0]

        var mu = median(values)                   // стартовое приближение центра — робастная медиана
        val s = mad(values).coerceAtLeast(1e-10)  // оценка масштаба (нижняя отсечка от деления на ноль)

        repeat(maxIter) {
            var sumW = 0.0                         // накопитель суммы весов (знаменатель)
            var sumWx = 0.0                        // накопитель суммы w*x (числитель)
            for (x in values) {
                val r = (x - mu) / s              // стандартизованный остаток наблюдения
                // Весовая функция Хьюбера: ядро (|r|<=k) -> вес 1, хвост -> убывающий вес k/|r|
                val w = if (abs(r) <= k) 1.0 else k / abs(r)
                sumW += w
                sumWx += w * x
            }
            if (sumW == 0.0) return mu            // вырожденный случай — возвращаем текущую оценку
            val muNew = sumWx / sumW              // новая оценка центра = взвешенное среднее
            // Критерий сходимости IRLS: относительное изменение mu меньше tol
            if (abs(muNew - mu) < tol * (1.0 + abs(mu))) return muNew
            mu = muNew
        }
        return mu                                 // достигнут лимит итераций — возвращаем последнее значение
    }

    // Фиксированные параметры по Приложению Е.2 диплома
    /** Константа состоятельности MAD для нормального распределения (Е.2). */
    const val MAD_CONSTANT = 1.4826
    /** Настроечная константа Хьюбера: ~95% эффективности при норме (Е.2). */
    const val HUBER_K = 1.345
    /** Порог сходимости IRLS (относительное изменение mu), 1e-6 по диплому. */
    private const val HUBER_TOL = 1e-6
    /** Предел числа итераций IRLS. */
    private const val HUBER_MAX_ITER = 100
}
