/*
 * НАЗНАЧЕНИЕ
 * Сырое значение оценки аудитории Aud_a (структурная ось влияния, этап 7).
 * Соответствует формуле Е.4.1: Aud_a = ln(1 + F_a).
 *
 * ЧТО ВНУТРИ
 * Объект AudienceComponent с единственной функцией computeRaw(followers).
 *
 * МЕТОД
 * Логарифмирование ln(1+F) сжимает тяжёлый правый хвост распределения числа
 * подписчиков (от десятков до миллионов), приводя его к более симметричному
 * виду перед робастной z-нормализацией. «+1» страхует от ln(0) при F=0.
 *
 * БИБЛИОТЕКИ
 * kotlin.math.ln. СВЯЗИ: F берётся из ClosedProfileImputer; результат идёт
 * на z-нормализацию и в структурный композит.
 */
package com.example.lomanalyzer.analysis.lom

import kotlin.math.ln

/**
 * A_raw = ln(1 + F) по автору из фонового (baseline) окна. Формула Е.4.1.
 */
object AudienceComponent {
    /**
     * Считает сырую оценку аудитории.
     * @param followers число подписчиков F автора.
     * @return ln(1 + F) — логарифмически сжатый размер аудитории.
     */
    fun computeRaw(followers: Int): Double =
        ln(1.0 + followers)  // лог-сжатие правого хвоста распределения подписчиков
}
