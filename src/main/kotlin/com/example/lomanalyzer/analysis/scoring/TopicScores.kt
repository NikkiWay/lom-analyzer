/*
 * НАЗНАЧЕНИЕ
 * Расчёт трёх оценок ОСИ 2 — «тематическая активность» автора (этап 7 алгоритма,
 * docs/algorithm.md; формулы Приложения Е.4.2). Оси описывают, насколько автор
 * вовлечён именно в исследуемую тему: объём тематических постов, степень
 * сосредоточенности на теме и совокупный охват.
 *
 * ЧТО ВНУТРИ
 * object TopicScores с тремя чистыми функциями:
 *  - topVol(count)                  — TopVol_a, число тематических постов;
 *  - topFocus(topic, nonTopic)      — TopFocus_a, доля темы в постах автора;
 *  - reach(viewsOrAudience)         — Reach_a, суммарный охват тематических постов.
 *
 * МЕТОД
 * TopVol_a   = |T_a|                                   — мощность множества тем. постов.
 * TopFocus_a = |T_a| / (|T_a| + |B_a^period|)          — доля темы среди всех постов
 *                                                        автора за период [0..1].
 * Reach_a    = Σ_{i∈T_a} V_i (или Σ A_i, если нет просмотров) — суммарный охват:
 *              число просмотров V_i, либо размер аудитории A_i как fallback.
 *
 * БИБЛИОТЕКИ
 * Только stdlib Kotlin — внешних зависимостей нет.
 *
 * СВЯЗИ
 * Вызывается из ScoringExecutor (этап 7); результаты сохраняются в LomScores
 * и z-нормализуются в CompositeScorer (ось 2 композита Topic_a).
 */
package com.example.lomanalyzer.analysis.scoring

/**
 * Оценки тематической активности (диплом Е.4, ось 2).
 *
 * Три оценки, вычисляемые по тематическим постам автора:
 * - TopVol_a:   число тематических постов (точечный счёт);
 * - TopFocus_a: доля темы в постах автора (точечный счёт);
 * - Reach_a:    охват по теме (выборочная оценка).
 */
object TopicScores {

    /**
     * TopVol_a = |T_a| — объём тематической активности (просто число тем. постов).
     * @param topicPostCount число подтверждённых тематических постов
     * @return |T_a|
     * @see Appendix E.4.2
     */
    fun topicalPostVolume(topicPostCount: Int): Int = topicPostCount

    /**
     * TopFocus_a = |T_a| / (|T_a| + |B_a^period|) — степень сосредоточенности на теме.
     *
     * Доля тематических постов среди всех постов автора за период темы.
     * @param topicPostCount |T_a| — число тематических постов
     * @param nonTopicPostCount |B_a^period| — нетематические посты за период темы
     * @return доля в [0..1]; 0.0, если постов нет вовсе
     * @see Appendix E.4.2
     */
    fun topicalFocusShare(topicPostCount: Int, nonTopicPostCount: Int): Double {
        // Знаменатель — все посты автора за период (тематические + фоновые)
        val total = topicPostCount + nonTopicPostCount
        // Нет постов за период → фокус не определён, возвращаем 0
        if (total <= 0) return 0.0
        // Доля темы в общем потоке постов автора
        return topicPostCount.toDouble() / total
    }

    /**
     * Reach_a = Σ_{i ∈ T_a} V_i (или Σ A_i, если просмотры недоступны) — суммарный охват.
     *
     * Если у поста есть число просмотров — берётся оно; иначе используется размер
     * аудитории (число подписчиков автора/сообщества на момент публикации).
     * Подстановка просмотров vs аудитории выполняется на стороне вызывающего кода
     * (ScoringExecutor), здесь — только суммирование уже подготовленных значений.
     *
     * @param viewsOrAudience список просмотров (или размеров аудитории) по тем. постам
     * @return суммарный охват по всем тематическим постам
     * @see Appendix E.4.2
     */
    fun topicalReach(viewsOrAudience: List<Long>): Double =
        // Совокупный охват — простая сумма охватов отдельных тематических постов
        viewsOrAudience.sum().toDouble()
}
