/*
 * НАЗНАЧЕНИЕ
 * Перечень возможных статусов сессии анализа (жизненный цикл сессии в пайплайне
 * из 10 этапов, см. docs/algorithm.md). Статус хранится в таблице
 * analysis_sessions и определяет, что приложение делает с сессией при старте
 * (восстановление, очистка) и что показывает в UI.
 *
 * ЧТО ВНУТРИ
 * enum SessionStatus с восемью состояниями: от создания до завершения, отмены
 * или ошибки. Используется SessionManager, PipelineOrchestrator, а также App.kt
 * при восстановлении «зависших» сессий после аварийного завершения.
 *
 * СВЯЗИ
 * Имя статуса (name) пишется в БД как строка; чтение обратно — через
 * SessionStatus.valueOf(...). См. SessionManager.getStatus и App.recoverStaleSessions.
 */
package com.example.lomanalyzer.orchestration

/**
 * Жизненный цикл сессии анализа. Каждое значение соответствует одному состоянию,
 * в котором может находиться сессия от момента создания до завершения.
 */
enum class SessionStatus {
    /** Сессия создана, но анализ ещё не запускался. */
    CREATED,

    /** Идёт сбор данных из VK (этапы 2–4 алгоритма). */
    COLLECTING,

    /** Идёт обработка/анализ собранных данных (этапы 5–10 алгоритма). */
    ANALYZING,

    /** Анализ приостановлен и ожидает восстановления (например, после сбоя). */
    PAUSED_PENDING_RECOVERY,

    /** Анализ полностью завершён успешно. */
    COMPLETED,

    /** Анализ не доведён до конца (прерван, но не помечен как ошибка/отмена). */
    INCOMPLETE,

    /** Анализ отменён пользователем. */
    CANCELLED,

    /** Анализ завершился с ошибкой. */
    FAILED,
}
