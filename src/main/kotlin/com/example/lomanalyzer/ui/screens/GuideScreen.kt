/**
 * Раскрываемая карточка-глава руководства: кликабельный заголовок с иконкой и
 * сворачиваемое тело.
 * @param icon иконка главы.
 * @param title заголовок главы.
 * @param content тело главы (composable-контент в ColumnScope).
 */
@Composable
@Suppress("FunctionNaming")
private fun GuideChapter(
    icon: ImageVector,
    title: String,
    content: @Composable ColumnScope.() -> Unit,
) {
    // Локальное состояние раскрытия главы (свёрнута по умолчанию)
    var expanded by remember { mutableStateOf(false) }
    Card(
        shape = RoundedCornerShape(12.dp),
        elevation = if (expanded) 2.dp else 1.dp,
        modifier = Modifier.fillMaxWidth(),
    ) {
        Column {
            // Header (clickable)
            Row(
                modifier = Modifier
                    .fillMaxWidth()
                    .clickable { expanded = !expanded }
                    .padding(16.dp),
                verticalAlignment = Alignment.CenterVertically,
            ) {
                Surface(
                    shape = RoundedCornerShape(8.dp),
                    color = AppColors.primaryLight,
                    modifier = Modifier.size(32.dp),
                ) {
                    Box(contentAlignment = Alignment.Center) {
                        Icon(icon, null, tint = AppColors.primary, modifier = Modifier.size(18.dp))
                    }
                }
                Spacer(Modifier.width(12.dp))
                Text(
                    title,
                    style = MaterialTheme.typography.subtitle1,
                    modifier = Modifier.weight(1f),
                )
                Icon(
                    if (expanded) Icons.Filled.ExpandLess else Icons.Filled.ExpandMore,
                    null,
                    tint = AppColors.textTertiary,
                )
            }

            // Body
            AnimatedVisibility(
                expanded,
                enter = expandVertically(),
                exit = shrinkVertically(),
            ) {
                Column(
                    modifier = Modifier.padding(start = 16.dp, end = 16.dp, bottom = 16.dp),
                    verticalArrangement = Arrangement.spacedBy(8.dp),
                    content = content,
                )
            }
        }
    }
}

/**
 * Крупный разделитель частей руководства: линии по бокам и подпись по центру.
 * @param label текст подписи раздела.
 */
@Composable
@Suppress("FunctionNaming")
