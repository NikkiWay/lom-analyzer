/**
 * Описание поля/термина: название жирным, затем тире и пояснение в одной строке.
 * @param field название поля/термина.
 * @param description пояснение.
 */
@Composable
@Suppress("FunctionNaming")
private fun FieldDesc(field: String, description: String) {
    Row(modifier = Modifier.fillMaxWidth().padding(vertical = 2.dp)) {
        Text(
            buildAnnotatedString {
                withStyle(SpanStyle(fontWeight = FontWeight.SemiBold, color = AppColors.textPrimary)) {
                    append(field)
                }
                append(" — $description")
            },
            style = MaterialTheme.typography.body2.copy(lineHeight = 20.sp, color = AppColors.textSecondary),
        )
    }
}

/** Нумерованный список пунктов (номера акцентным цветом). */
@Composable
@Suppress("FunctionNaming")
private fun NumberedList(vararg items: String) {
    Column(verticalArrangement = Arrangement.spacedBy(4.dp)) {
        items.forEachIndexed { index, item ->
            Row(modifier = Modifier.padding(start = 4.dp)) {
                Text(
                    "${index + 1}. ",
                    style = MaterialTheme.typography.body2.copy(fontWeight = FontWeight.SemiBold),
                    color = AppColors.primary,
                )
                Text(item, style = MaterialTheme.typography.body2, lineHeight = 20.sp)
            }
        }
    }
}
