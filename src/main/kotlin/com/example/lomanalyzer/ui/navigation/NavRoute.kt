/*
 * НАЗНАЧЕНИЕ
 * Перечень всех экранов (маршрутов) приложения LOM Analyzer. Это «адресная книга»
 * UI: каждое значение enum соответствует одному экрану в боковом меню и одному
 * ветвлению в when-роутере ContentArea (см. файл MainContent.kt). Соответствует
 * разделу диплома 2.2.6 (семь основных экранов плюс вспомогательные).
 *
 * ЧТО ВНУТРИ
 * Единственный enum NavRoute с 10 значениями. Маршруты сгруппированы по смыслу:
 * подготовка и сбор (SETUP, COLLECTION, TOPIC_VALIDATION, SESSION_HISTORY),
 * результаты (LOM_DASHBOARD, LOM_DETAIL, SESSION_QUALITY, SESSION_LOG) и
 * вспомогательные (GUIDE, PROFILE).
 *
 * СВЯЗИ
 * AppNavigator хранит текущий NavRoute в StateFlow; Sidebar (MainContent.kt)
 * рисует пункты меню по этим значениям; ContentArea выбирает Composable-экран.
 * Привязка к этапам пайплайна (docs/algorithm.md): SETUP — этап 1 (постановка),
 * COLLECTION — сбор и анализ (этапы 2-10), TOPIC_VALIDATION — этап 6 (валидация
 * тематической фильтрации аналитиком), остальные — отображение результатов.
 */
package com.example.lomanalyzer.ui.navigation

/**
 * Маршруты навигации UI LOM Analyzer.
 * Семь основных экранов по разделу диплома 2.2.6 плюс вспомогательные экраны.
 */
enum class NavRoute {
    /** Форма постановки задачи и создания сессии (этап 1 пайплайна) */
    SETUP,
    /** Экран хода сбора и анализа данных в реальном времени (этапы 2-10) */
    COLLECTION,
    /** Сводка качества тематической фильтрации и ручная валидация аналитиком (этап 6) */
    TOPIC_VALIDATION,
    /** Список ранее выполненных сессий анализа */
    SESSION_HISTORY,
    /** Главный дашборд: таблица авторов и scatter-диаграмма квадрантов */
    LOM_DASHBOARD,
    /** Карточка одного автора с детализацией метрик */
    LOM_DETAIL,
    /** Индикаторы качества сессии (этап 8) */
    SESSION_QUALITY,
    /** Руководство пользователя */
    GUIDE,
    /** Профиль и настройки приложения */
    PROFILE,
    /** Журнал событий сессии */
    SESSION_LOG,
}
