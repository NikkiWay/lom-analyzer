/*
 * НАЗНАЧЕНИЕ
 * Псевдонимизация персональных данных (PII): необратимое хэширование
 * идентификаторов (например, VK-id) для хранения и анализа без раскрытия
 * исходных значений. Часть модуля безопасности и защиты персональных данных.
 *
 * ЧТО ВНУТРИ
 * Объект PiiHasher с единственной функцией hash(value, sessionSalt) —
 * солёный SHA-256-хэш значения в виде hex-строки.
 *
 * МЕТОД
 * SHA-256 от конкатенации «соль сессии + значение». Соль сессии (sessionSalt)
 * не секрет, но делает хэши непереносимыми между сессиями и затрудняет атаки
 * по словарю/радужным таблицам. Хэширование одностороннее — восстановить
 * исходное значение из хэша нельзя.
 *
 * БИБЛИОТЕКИ
 * java.security.MessageDigest (SHA-256) — стандартная криптобиблиотека JDK.
 */
package com.example.lomanalyzer.security

import java.security.MessageDigest

/** Утилита необратимого солёного хэширования персональных идентификаторов. */
object PiiHasher {
    /**
     * Возвращает SHA-256-хэш значения с солью сессии в виде hex-строки.
     *
     * @param value исходное значение (например, VK-id) для псевдонимизации.
     * @param sessionSalt соль сессии, подмешиваемая перед значением.
     * @return шестнадцатеричное представление 32-байтового хэша.
     */
    fun hash(value: String, sessionSalt: ByteArray): String {
        val digest = MessageDigest.getInstance("SHA-256")
        // Сначала в хэш подаётся соль сессии, затем сами байты значения (UTF-8)
        digest.update(sessionSalt)
        digest.update(value.toByteArray(Charsets.UTF_8))
        // Переводим 32 байта дайджеста в hex-строку (по два символа на байт)
        return digest.digest().joinToString("") { "%02x".format(it) }
    }
}
