-- ============================================================
-- V13: sentiment_result — распределение вероятностей по классам
-- ============================================================
--
-- ЗАЧЕМ
-- Модель тональности возвращает вероятности всех трёх классов, но сохранялась
-- только метка-победитель и её уверенность. У сдержанного текста распределение
-- вида neutral 0.80 / positive 0.15 / negative 0.05 сводилось к одной метке
-- «neutral», и склонность к позитиву терялась безвозвратно.
--
-- Это делало оси позиции автора (Pos_a) и отклика аудитории (Resp_a)
-- нечувствительными: у автора с одним тематическим постом Pos_a получался
-- ровно (0, 1, 0) — вырожденное распределение без какого-либо перевеса.
--
-- Колонки заполняются только моделью sidecar. Словарный fallback распределения
-- не даёт: у него метка либо есть, либо нет, поэтому там остаётся NULL, и
-- расчёт для таких строк возвращается к долям меток (см. PositionScore).

ALTER TABLE sentiment_result ADD COLUMN prob_positive REAL;
ALTER TABLE sentiment_result ADD COLUMN prob_neutral  REAL;
ALTER TABLE sentiment_result ADD COLUMN prob_negative REAL;

-- Те же колонки в кэше NLP: без них попадание в кэш возвращало бы результат без
-- распределения, и повторный прогон той же сессии откатывался бы к расчёту по
-- меткам — то есть исправление работало бы ровно один раз.
ALTER TABLE nlp_result ADD COLUMN prob_positive REAL;
ALTER TABLE nlp_result ADD COLUMN prob_neutral  REAL;
ALTER TABLE nlp_result ADD COLUMN prob_negative REAL;
