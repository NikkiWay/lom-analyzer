-- ============================================================
-- V14: удаление неиспользуемого столбца analysis_session.role_mode
-- ============================================================
--
-- ОБОСНОВАНИЕ
-- Столбец добавлен в V5 под выбор способа классификации ролей: квадранты либо
-- кластеризация GMM. Экран настройки предлагал оба варианта, значение доходило до
-- этого столбца — но ни один этап анализа его не читал. Роли всегда назначались
-- по квадрантам (RoleAssigner), реализации GMM в проекте нет, а кластеризация
-- исключена из метода намеренно: роль описывает положение автора относительно
-- порогов сессии, а не принадлежность к найденному кластеру.
--
-- Столбец объявлен NOT NULL DEFAULT 'QUADRANT' и никем, кроме вставки сессии, не
-- заполнялся, поэтому во всех строках стоит 'QUADRANT'. Значение постоянно, и его
-- удаление не приводит к потере сведений.
--
-- Переключатель на экране настройки, параметр SessionParams.roleMode и запись
-- столбца удалены вместе с ним, чтобы схема БД и код не расходились.

ALTER TABLE analysis_session DROP COLUMN role_mode;
