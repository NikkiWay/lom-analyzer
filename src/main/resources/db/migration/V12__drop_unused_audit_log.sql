-- ============================================================
-- V12: удаление неиспользуемой таблицы audit_log
-- ============================================================
--
-- ОБОСНОВАНИЕ
-- Таблица создавалась в V1 под журнал аудита (класс AuditLog + AuditLogDao), но
-- метод AuditLog.record() не вызывался ни из одного места приложения, а вставка
-- в audit_log была достижима только через него. Поэтому таблица гарантированно
-- пуста при любой истории использования — удаление не может привести к потере
-- данных.
--
-- Аудит значимых действий фактически ведётся через структурированный Logger и
-- перечисление AppEvent (например, AppEvent.RAW_EXPORT_CONFIRMED пишется в
-- CsvExporter при выгрузке неанонимизированных данных). AuditLog дублировал этот
-- механизм: он писал ту же запись в тот же Logger и дополнительно в БД.
--
-- Соответствующие классы (AuditLog, AuditDao, AuditLogDao) удалены вместе с их
-- регистрациями в Koin, чтобы схема БД и код не расходились.

DROP INDEX IF EXISTS idx_audit_session_time;
DROP TABLE IF EXISTS audit_log;
