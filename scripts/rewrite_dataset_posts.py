def main():
    """Перезаписывает CURRENT-посты, чистит BASELINE и комментарии, сохраняет датасет."""
    dataset_path = Path(__file__).parent.parent / "examples" / "dataset_ai_v2.json"
    with open(dataset_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    rng = random.Random(42)  # фиксированный seed — воспроизводимый выбор текстов

    # Индекс авторов по vkId
    authors = {a["vkId"]: a for a in data["authors"]}

    # ── Перезапись CURRENT-постов ──
    # Группируем CURRENT-посты по автору, сохраняя индекс поста в общем массиве
    current_by_author = {}
    for i, p in enumerate(data["posts"]):
        if p["window"] == "CURRENT":
            current_by_author.setdefault(p["fromId"], []).append((i, p))

    changes = 0
    # Множество уже использованных текстов — чтобы максимизировать уникальность
    globally_used = set()

    def make_unique(base_text):
        """Возвращает уникальный (ещё не использованный) вариант текста.

        Сначала пробует сам текст и вариант с нормализованной точкой на конце,
        затем добавляет один из вводных префиксов, и в крайнем случае дописывает
        случайный числовой суффикс. Гарантирует отсутствие точных дублей.
        """
        # Мелкие правки, сохраняющие естественность текста
        variants = [
            base_text,
            base_text.rstrip('.') + '.',
        ]
        for v in variants:
            if v not in globally_used:
                return v
        # Добавляем вводный префикс (со строчной первой буквой исходного текста)
        prefixes = [
            "Кстати, ", "К слову, ", "Между прочим, ", "Заметил: ",
            "Наблюдение: ", "Опыт показывает: ", "По моему опыту, ",
            "Вот что заметил: ", "Интересная ситуация: ",
        ]
        for pfx in rng.sample(prefixes, len(prefixes)):
            variant = pfx + base_text[0].lower() + base_text[1:]
            if variant not in globally_used:
                return variant
        # Крайний случай — дописываем уникальный числовой маркер
        return base_text + f" #{rng.randint(1000,9999)}"

    for author_id, posts in current_by_author.items():
        if author_id in AUTHOR_POSTS:
            # У автора есть персональный набор — раздаём его посты по порядку
            texts = AUTHOR_POSTS[author_id]
            for j, (idx, post) in enumerate(posts):
                if j < len(texts):
                    text = texts[j]
                    if text in globally_used:
                        text = make_unique(text)  # дубль — делаем уникальным
                    globally_used.add(text)
                    data["posts"][idx]["text"] = text
                    changes += 1
                else:
                    # Персональные тексты кончились — берём из общего пула
                    available = [t for t in GENERIC_POSTS if t not in globally_used]
                    if not available:
                        text = make_unique(rng.choice(GENERIC_POSTS))  # пул исчерпан — варьируем
                    else:
                        text = rng.choice(available)
                    globally_used.add(text)
                    data["posts"][idx]["text"] = text
                    changes += 1
        else:
            # У автора нет персонального набора — все посты из общего пула
            for j, (idx, post) in enumerate(posts):
                available = [t for t in GENERIC_POSTS if t not in globally_used]
                if not available:
                    text = make_unique(rng.choice(GENERIC_POSTS))
                else:
                    text = rng.choice(available)
                globally_used.add(text)
                data["posts"][idx]["text"] = text
                changes += 1

    # ── Чистим и BASELINE-посты — убираем очевидные шаблонные «AI-паттерны» ──
    baseline_changes = 0
    for i, p in enumerate(data["posts"]):
        if p["window"] == "BASELINE":
            text = p.get("text", "")
            # Признаки сгенерированного/шаблонного текста (призывы к обсуждению и т.п.)
            bad_patterns = ["Что скажете?", "Делитесь опытом", "Кто что думает?",
                          "Жду комментариев", "Интересно ваше мнение", "Согласны?",
                          "Пишите в комменты", "Ваше мнение?", "Как думаете?",
                          "не знаю что сказать", "well, AI is"]
            # Перезаписываем, если есть шаблон, пустой текст или артефакт «иИ»
            needs_rewrite = any(bp in text for bp in bad_patterns) or not text.strip() or "иИ" in text
            if needs_rewrite:
                data["posts"][i]["text"] = rng.choice(BASELINE_FILLERS)
                baseline_changes += 1

    # ── Чистим комментарии — убираем повторяющиеся шаблонные паттерны ──
    comment_positive = [
        "Согласен, полезная штука", "Интересный опыт, спасибо за пост",
        "Мы тоже внедряли, результат хороший", "Наконец-то кто-то разумное пишет",
        "Подтверждаю, сам пробовал", "Полезно, сохранил себе",
        "Тоже заметил это улучшение", "Да, прогресс очевиден",
        "Отличная мысль, поддерживаю", "Взял на заметку, буду тестировать",
    ]
    comment_neutral = [
        "Спорный вопрос, но есть над чем подумать", "Время покажет",
        "Не уверен, что всё так однозначно", "Интересно, но нужно больше данных",
        "Надо смотреть на конкретные кейсы", "Тут много нюансов",
        "Посмотрим через год", "Сложная тема, однозначного ответа нет",
    ]
    comment_negative = [
        "Это маркетинг, а не реальный прорыв", "Пользовался, не впечатлило",
        "Переоцениваете возможности нейросетей", "Звучит красиво, но на практике не работает",
        "Пока это игрушка, а не инструмент", "Пузырь лопнет рано или поздно",
    ]
    # Смешанный пул замен с перекосом в нейтрал/позитив (умножение = вес тональности)
    all_comments = comment_positive * 3 + comment_neutral * 4 + comment_negative * 2

    comment_changes = 0
    for i, c in enumerate(data["comments"]):
        text = c.get("text", "")
        # Признаки шаблонного/битого комментария
        bad = any(bp in text for bp in ["Что скажете", "иИ", "Жду комментариев", "Делитесь"])
        if not text.strip() or bad:
            data["comments"][i]["text"] = rng.choice(all_comments)
            comment_changes += 1

    # ── Сохранение ──
    output_path = Path(__file__).parent.parent / "examples" / "dataset_ai_v2.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Rewritten {changes} CURRENT posts")
    print(f"Cleaned {baseline_changes} BASELINE posts")
    print(f"Cleaned {comment_changes} comments")
    print(f"Saved to {output_path}")

    # Проверяем итоговую долю уникальных CURRENT-текстов
    current_texts = [p["text"] for p in data["posts"] if p["window"] == "CURRENT"]
    unique = len(set(current_texts))
    print(f"\nUniqueness: {unique}/{len(current_texts)} unique CURRENT posts ({unique/len(current_texts)*100:.1f}%)")


if __name__ == "__main__":
    main()
