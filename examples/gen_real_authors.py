import json, random
from collections import Counter

with open('examples/ai_dataset.json', encoding='utf-8') as f:
    data = json.load(f)

next_id = max(p['vkId'] for p in data['posts']) + 1

baseline_start = 1774569600
baseline_end   = 1777161600
current_start  = 1777161600
current_end    = 1779753600

random.seed(123)

communities = [-100001, -100002, -100003, -100005, -100006, -100007, -100008]

def make_posts(author_id, texts, window, likes_r, reposts_r, comments_r, views_r,
               media_p=0.3, copy_p=0.0, owner_ids=None):
    global next_id
    posts = []
    for text in texts:
        ts_s = baseline_start if window == "BASELINE" else current_start
        ts_e = baseline_end if window == "BASELINE" else current_end
        oid = random.choice(owner_ids or communities)
        posts.append({
            "vkId": next_id, "ownerId": oid, "fromId": author_id,
            "date": random.randint(ts_s, ts_e), "text": text,
            "likes": random.randint(*likes_r), "reposts": random.randint(*reposts_r),
            "comments": random.randint(*comments_r), "views": random.randint(*views_r),
            "containsMedia": random.random() < media_p,
            "hasCopyHistory": random.random() < copy_p, "window": window
        })
        next_id += 1
    return posts

